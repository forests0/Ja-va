package ch05;

public class CharTest {

	public static void main(String[] args) {
		char ch1 = 'A';
		System.out.println(ch1);
		System.out.println((int)ch1);

		char ch2 = 66;
		System.out.println(ch2);
		
		int ch3 = 67;
		System.out.println((char)ch3);
		
		char ch5 = 'วั';
		System.out.println(ch5);
		
		char ch6 = '\uD55C';
		System.out.println(ch6);
	}

}
